from zhipuai import ZhipuAI

def load_api_key():
    config_file = 'config.json'
    if os.path.exists(config_file):
        with open(config_file, 'r') as file:
            config = json.load(file)
            return config.get('api_key'), config.get('api_choice'), config.get('agreed', False)
    return None, None, False

def save_api_key(api_key, api_choice, agreed):
    config = {
        'api_key': api_key,
        'api_choice': api_choice,
        'agreed': agreed
    }
    with open('config.json', 'w') as file:
        json.dump(config, file)

def clear_config():
    config_file = 'config.json'
    if os.path.exists(config_file):
        os.remove(config_file)
        print("配置文件已清空")
    else:
        print("配置文件不存在")

def create_project(address, project_name):
    path = os.path.join(address, project_name)
    os.makedirs(path, exist_ok=True)
    print(f"项目文件夹创建成功，路径为：{path}")

def create_n_file(address, file_name):
    path = os.path.join(address, file_name + '.n')
    with open(path, 'w', encoding='utf-8') as file:
        file.write('')
    print(f"文件创建成功，路径为：{path}")

def open_n_file_with_notepad(address):
    subprocess.run(['notepad.exe', address])

def transform_code(address, key, choose):
    with open(address, 'r', encoding='utf-8') as file:
        code = file.read()
    
    prompt = f"请将以下给出的自然语言转为python语句，不需要任何解释：{code}"
    
    response = None
    if choose == "1":
        client = ZhipuAI(api_key=key)
        model_name = "glm-4"
        try:
            response = client.chat.completions.create(
                model=model_name,
                messages=[
                    {"role": "user", "content": prompt}
                ],
            )
        except Exception as e:
            print(f"API调用失败: {e}")
            return
    elif choose == "2":
        import openai
        openai.api_key = key
        try:
            response = openai.Completion.create(
                engine="davinci-codex",
                prompt=prompt,
            )
        except Exception as e:
            print(f"API调用失败: {e}")
            return
    
    if response is None:
        print("API调用未能返回结果，请检查您的API Key和网络连接")
        return
    
    print(response)
    
    try:
        if hasattr(response, 'choices') and response.choices:
            choice = response.choices[0]
            if hasattr(choice, 'message') and hasattr(choice.message, 'content'):
                message_content = choice.message.content.replace("`", "").replace("python", "")
            else:
                message_content = choice.text.replace("`", "").replace("python", "")
                
            new_address = os.path.join(os.path.dirname(address), os.path.splitext(os.path.basename(address))[0] + '.py')
            with open(new_address, 'w', encoding='utf-8') as file:
                file.write(message_content.strip())
            print(f"代码转换成功，路径为：{new_address}")
        else:
            print("API返回的对象没有choices属性或为空")
    except (AttributeError, KeyError) as e:
        print(f"处理API返回结果时出错: {e}")

def run_python_script(address):
    new_path = address.replace('.n', '.py')
    subprocess.run(['python', new_path])

def install_package(package_name):
    bat_folder = 'bat'
    if not os.path.exists(bat_folder):
        os.makedirs(bat_folder)
    
    bat_file_path = os.path.join(bat_folder, 'install.bat')
    with open(bat_file_path, 'w') as bat_file:
        bat_file.write(f'pip install {package_name}')
    
    subprocess.run([bat_file_path], shell=True)
    
